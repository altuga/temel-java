public class WhileOrnek {

    public static void main(String args[]) {

         int i = 0 ;    //döngü kontrol degiskeni
         while (i < 10 ) 
	 {
             System.out.println("i = " + i);
             i++ ;
         }
         System.out.println("Sayma islemi tamamlandi.");
    }

}


